export const categories = [...new Set(osData.map(os => os.category))];
export const developers = [...new Set(osData.map(os => os.developer))];

export const getOSById = (id) => osData.find(os => os.id === id);
export const getOSByCategory = (category) => osData.filter(os => os.category === category);
export const searchOS = (query) => {
  const q = query.toLowerCase();
  return osData.filter(os =>
    os.name.toLowerCase().includes(q) ||
    os.developer.toLowerCase().includes(q) ||
    os.category.toLowerCase().includes(q) ||
    os.kernel.toLowerCase().includes(q) ||
    String(os.releaseYear).includes(q)
  );
};
